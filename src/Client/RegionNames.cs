namespace Client
{
    /// <summary>
    /// Provides a set of strongly typed region names.
    /// </summary>
    public class RegionNames
    {
        /// <summary>
        /// Gets the event selection region name.
        /// </summary>
        public const string EventSelectionRegion = nameof(EventSelectionRegion);

        /// <summary>
        /// Gets the event track region name.
        /// </summary>
        public const string TraceRegion = nameof(TraceRegion);

        /// <summary>
        /// Gets the event information region name.
        /// </summary>
        public const string InformationBarRegion = nameof(InformationBarRegion);

        /// <summary>
        /// Gets the event reference time region name.
        /// </summary>
        public const string ReferenceTimeRegion = nameof(ReferenceTimeRegion);
    }
}
